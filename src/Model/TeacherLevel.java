package Model;


public class TeacherLevel {
    
    private int idTeacher;
    private int level;
    private float price;

    public int getId() {
        return idTeacher;
    }

    public void setId(int id) {
        this.idTeacher = id;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
    
    
}
